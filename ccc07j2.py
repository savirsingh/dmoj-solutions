notcompleted = True

while notcompleted:
    line = input()
    if line == "CU":
        print("see you")
    elif line == ":-)":
        print("I'm happy")
    elif line == ":-(":
        print("I'm unhappy")
    elif line == ";-)":
        print("wink")
    elif line == ":-P":
        print("stick out my tongue")
    elif line == "(~.~)":
        print("sleepy")
    elif line == "TA":
        print("totally awesome")
    elif line == "CCC":
        print("Canadian Computing Competition")
    elif line == "CUZ":
        print("because")
    elif line == "TY":
        print("thank-you")
    elif line == "YW":
        print("you're welcome")
    elif line == "TTYL":
        print("talk to you later")
        notcompleted = False
    else:
        print(line)
